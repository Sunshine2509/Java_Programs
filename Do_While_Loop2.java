// Java Program, to print the table of 2 using do while loop.

class Do_While_Loop2
{
	// Main method
	public static void main(String args[])
	{
		// Declaration and initialization of variable

		int n=1;
		
		// do while loop
		do
		{
			System.out.println(2*n);
			n++;
		}while(n<=10);
	}
}

