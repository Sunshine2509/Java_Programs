/* Default Constructor  : 1) It is a type of constructor which is created by the compiler ,
						  when a programmer fails to add any kind of constructor in a class.

						  2)  Basically default constructor are no argument constructor which does not accept any input data. */


	class DefaultConstructor
	{
	
	}

	class Constructor
	{
		public static void main(String[] args) {

			DefaultConstructor obj=new DefaultConstructor();
		}
	}


	// In the above program compiler will add a default constructor of class DefaultConstructor .