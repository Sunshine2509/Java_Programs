// Java Program, to print the numbers from 1 to 10 using do while loop.

class Do_While_Loop1
{
	// Main method
	public static void main(String args[])
	{
		// Declaration and initialization of variable

		int n=1;
		
		// do while loop
		do
		{
			System.out.println(n);
			n++;
		}while(n<=10);
	}
}

