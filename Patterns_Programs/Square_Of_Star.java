 class Square_Of_Star

{
	// Main Method

	public static void main(String args[])
	{
		//Outer for loop 

		for ( int i=1; i<=4; i++)
		{
			// Inner for loop 

			for( int j=1;j<=4;j++)
			{
				System.out.print(" "+"*");
			}
				// for next line 

				System.out.println();
		}
	}
}


