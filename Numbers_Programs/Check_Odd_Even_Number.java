/* Odd numbers : Odd numbers are the numbers that cannot be completely divided by 2.

   Even numbers : Any number that can be exactly divided by 2 is called as an even number. */


class Check_Odd_Even_Number
{

	// Main method

	public static void main(String args[])
	{
		// Declaration and initialization of the variable

		int num= 2;

		// condition for checking even or odd number 

		if(num%2==0)
		{
			System.out.println("Even number");
		}
		else
		{
			System.out.println("Odd number");
		}
	}
}


	/* Output : 
		
		Compile time: Success

		Runtime : Success and Even number */		